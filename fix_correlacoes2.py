nova.append('                    cor = "#2e6b3e"; icone = "Interno ACIMA do publico"; msg = "Oportunidade de amplificar reputacao"')
nova.append('                elif div < -10:')
nova.append('                    cor = VERMELHO; icone = "Publico ACIMA do interno"; msg = "Atencao — cliente insatisfeito nao aparece na pesquisa?"')
nova.append('                else:')
nova.append('                    cor = "#B8923A"; icone = "Alinhado"; msg = "Percepcao interna e externa consistentes"')
nova.append('                st.markdown(f\'<div style="padding:10px 0; border-bottom:1px solid #e8ddc8;">')
