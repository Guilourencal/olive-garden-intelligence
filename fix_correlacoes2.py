nova.append('        st.markdown(\'<div class="section-title">Divergencias — Percepcao Interna vs Externa</div>\', unsafe_allow_html=True)')
nova.append('        st.markdown(\'<div style="font-size:12px; color:#8B7A5A; margin-bottom:16px;">Onde o cliente esta saindo diferente do que a pesquisa interna indica. Divergencia alta = risco de reputacao.</div>\', unsafe_allow_html=True)')
