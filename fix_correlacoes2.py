nova.append('                cor = VERDE if v >= 80 else "#B8923A" if v >= 70 else VERMELHO')
nova.append('                status = "SAUDAVEL" if v >= 80 else "ATENCAO" if v >= 70 else "CRITICO"')
nova.append('                pct = int((v / 100) * 100)')
nova.append('                st.markdown(f\'\'\'<div style="margin-bottom:14px;">')
nova.append('                    <div style="display:flex; justify-content:space-between; align-items:center; margin-bottom:6px;">')
nova.append('                    <span style="font-size:13px; font-weight:700; color:#3D2B1F;">{row["filial_curta"]}</span>')
nova.append('                    <div style="display:flex; align-items:center; gap:12px;">')
nova.append('                    <span style="font-size:11px; font-weight:700; color:{cor}; background:rgba(0,0,0,0.05); padding:2px 10px; border-radius:20px;">{status}</span>')
nova.append('                    <span style="font-size:13px; font-weight:700; color:{cor};">{v:.1f}</span>')
nova.append('                    </div></div>')
nova.append('                    <div style="background:#e8ddc8; border-radius:4px; height:8px;">')
nova.append('                    <div style="background:{cor}; width:{pct}%; height:8px; border-radius:4px;"></div>')
