nova.append('            ))')
nova.append('            fig_scatter.update_layout(')
nova.append('                paper_bgcolor="rgba(0,0,0,0)", plot_bgcolor="rgba(0,0,0,0)",')
nova.append('                margin=dict(t=10, b=10, l=10, r=10),')
nova.append('                xaxis=dict(title="GSS — Experiencia Geral (% Topbox)", tickfont=dict(family="Nunito", size=11, color=MARROM), gridcolor="#E8DCC8"),')
nova.append('                yaxis=dict(title="Reputacao Publica (Indice)", tickfont=dict(family="Nunito", size=11, color=MARROM), gridcolor="#E8DCC8"),')
nova.append('                font=dict(family="Nunito"),')
nova.append('                height=380,')
nova.append('            )')
nova.append('            st.plotly_chart(fig_scatter, use_container_width=True, key="fig_scatter")')
nova.append('')
nova.append('    st.markdown("<br>", unsafe_allow_html=True)')
nova.append('')
nova.append('    # Tendencia GSS por dimensao')
nova.append('    with st.container(border=True):')
nova.append('        st.markdown(\'<div class="section-title">Tendencia GSS por Dimensao — Rede Consolidada</div>\', unsafe_allow_html=True)')
nova.append('        st.markdown(\'<div style="font-size:12px; color:#8B7A5A; margin-bottom:16px;">Evolucao media de cada dimensao considerando todas as filiais. Dimensoes em queda requerem atencao imediata.</div>\', unsafe_allow_html=True)')
nova.append('        if len(df_perf_c["periodo_curto"].dropna().unique()) > 1:')
nova.append('            metricas_t = {"overall_experience": "Exp. Geral", "value": "Valor", "service": "Atendimento", "taste": "Sabor", "speed_of_service": "Velocidade", "clean": "Limpeza", "soup_salad_refill": "Refil Sopa", "breadstick_refill": "Refil Bread"}')
nova.append('            trend_df = df_perf_c.groupby("periodo_curto")[list(metricas_t.keys())].mean().reset_index().sort_values("periodo_curto")')
nova.append('            fig_trend = go.Figure()')
